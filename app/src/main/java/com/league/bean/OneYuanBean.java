public class OneYuanBean {
    private String mName;
    private int mPeriod;
    private Long mTotalPeo;
    private Long mTakingPeo;

    public String getmName() {
        return mName;
    }

    public void setmName(String mName) {
        this.mName = mName;
    }

    public Long getmTotalPeo() {
        return mTotalPeo;
    }

    public void setmTotalPeo(Long mTotalPeo) {
        this.mTotalPeo = mTotalPeo;
    }

    public int getmPeriod() {
        return mPeriod;
    }

    public void setmPeriod(int mPeriod) {
        this.mPeriod = mPeriod;
    }

    public Long getmTakingPeo() {
        return mTakingPeo;
    }

    public void setmTakingPeo(Long mTakingPeo) {
        this.mTakingPeo = mTakingPeo;
    }
}
