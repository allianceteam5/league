public class PassAnnouncedBean {
    private String thumb;
    private int peroid;
    private String  time;
    private String holderName;
    private String holderId;
    private String luckNum;
    private int takPeoNum;

    public String getThumb() {
        return thumb;
    }

    public void setThumb(String thumb) {
        this.thumb = thumb;
    }

    public int getPeroid() {
        return peroid;
    }

    public void setPeroid(int peroid) {
        this.peroid = peroid;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getHolderName() {
        return holderName;
    }

    public void setHolderName(String holderName) {
        this.holderName = holderName;
    }

    public String getHolderId() {
        return holderId;
    }

    public void setHolderId(String holderId) {
        this.holderId = holderId;
    }

    public String getLuckNum() {
        return luckNum;
    }

    public void setLuckNum(String luckNum) {
        this.luckNum = luckNum;
    }

    public int getTakPeoNum() {
        return takPeoNum;
    }

    public void setTakPeoNum(int takPeoNum) {
        this.takPeoNum = takPeoNum;
    }
}
